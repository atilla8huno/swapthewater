    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed

        int confirmacao = JOptionPane.showConfirmDialog(this, "Confirma exclusão do(a) "+ jTextFieldNomeVoluntario.getText() +"?", 
                    "Excluir Voluntário?", JOptionPane.YES_NO_OPTION);
        
        if(!jTextFieldIdVoluntario.getText().equals("0") && confirmacao == JOptionPane.YES_OPTION){
            try {
                Voluntario v = vDAO.getVoluntario(Integer.parseInt(jTextFieldIdVoluntario.getText()));
                modelTabela.excluir(v);
                Main.trocadores.remove(v);
                
                //Em caso de nenhum cadastro
                if(Main.trocadores.size() == 0){
                    Main.jTextFieldProximoTrocador.setText("");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao tentar excluir! Erro: "+ ex.getMessage());
            }
            limpaTela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione alguém, neh...");
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

