    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed

        if(jTextFieldNomeVoluntario.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Informe o nome, neh...");
        } else {
            Integer id  = Integer.parseInt(jTextFieldIdVoluntario.getText());
            String nome = jTextFieldNomeVoluntario.getText().toUpperCase();
            
            voluntario.setId(id);
            voluntario.setNome(nome);
            modelTabela.salvar(voluntario);
            
            Main.atualizaVoluntarios();
            limpaTela();
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

